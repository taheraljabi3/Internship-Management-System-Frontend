export function translateText(text, isArabic) {
  if (!isArabic || text === null || text === undefined) return text;

  const normalized = typeof text === 'string' ? text.trim() : text;

  if (translations[normalized]) {
    return translations[normalized];
  }

  // دعم الحالات التي تأتي من الباك إند بصيغ مختلفة مثل UnderReview أو student_profile
  if (typeof normalized === 'string') {
    const humanized = normalized
      .replace(/_/g, ' ')
      .replace(/([a-z])([A-Z])/g, '$1 $2')
      .replace(/\s+/g, ' ')
      .trim();

    if (translations[humanized]) {
      return translations[humanized];
    }

    const titleCase = humanized
      .split(' ')
      .map((word) => word ? word.charAt(0).toUpperCase() + word.slice(1) : word)
      .join(' ');

    if (translations[titleCase]) {
      return translations[titleCase];
    }
  }

  return text;
}